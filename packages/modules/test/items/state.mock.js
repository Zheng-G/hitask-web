export const withCompletedItems = (state, itemIds = []) => {
	const newState = { ...state };
	newState.items.items = Object.keys(state.items.items).reduce((acc, itemId) => {
		const item = state.items.items[itemId];
		acc[itemId] = item;
		if (itemIds.indexOf(item.id) !== -1) {
			acc[itemId].completed = true;
		}
		return acc;
	}, {});
	return newState;
};

